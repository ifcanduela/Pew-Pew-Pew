<h1>Obligatory, Albeit Brief, Introduction</h1>

<p>Did you know kittens <em>don't</em> like web programming? They think it's a
mess, with all those files, URLs, browsers and cascading style sheets, not to
mention <em>Java</em>. They are right, as they usually are, but this time they
are showing grok-level understanding of teh intarwebz.</p>

<p><strong class="pewpewpew">Pew-Pew-Pew</strong> is a very simple framework. At
its most basic, it will allow you to drop <abbr title="Hypertext Mark-up
Language">HTML</abbr>-formatted <abbr title="PHP: Hypertext Preprocessor">PHP
</abbr> files (like this very document you're reading) into a folder and browse
through them. However, <strong class="pewpewpew">Pew-Pew-Pew</strong> can also
simplify database access and session handling, and save some cats along the
way.</p>

<p>To be honest, it's best suited for projects like your sister's
<strong>Pok√©mon collection manager</strong>. No <a href="maps.google.com">Google
Maps</a> will be built with this piece of incomplete software, <em>no sir</em>.
Apart from that, you can count on it not slowing you down. If you're a good
programmer, you will be as good as you were before. If you are a bad programmer,
maybe you can learn something.</p>

<p>There's nothing especially revolutionary in <strong
class="pewpewpew">Pew-Pew-Pew</strong>. It follows <a
href="http://cakephp.org">CakePHP</a>'s and <a
href="http://codeigniter.com">CodeIgniter</a>'s steps for no-fuss configuration
and accessibility for new users. I wouldn't call it an MVC framework, since that
assumes it provides simple things like sanitization, caching or REST &mdash; 
and it doesn't. The thing is, it's very easy to add them yourself, if you know
what you're doing. But be mindful that this is for <em>people who like doing
things their way</em>.</p>
