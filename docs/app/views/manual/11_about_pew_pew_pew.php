<h1>About Pew-Pew-Pew</h1>
            
<p>So this is the last chapter of the manual, and you're wondering if you're
missing something. "Why would I choose this over CodeIgniter, CakePHP, Symphony,
Kohana, Yii, Zend, Fuel, Lithium or even Drupal, Joomla or
Wordpress?"<sup>[1]</sup>. It's hard for me to come up with an answer, really.
I did this for the learning value, and I must say I'm a better PHP programmer
now compared to that what I was before starting the project. My advice is
"don't use <strong class="pewpewpew">Pew-Pew-Pew</strong>, copy it and then
improve upon it".</p>

<p>The name of the framework is unimportant, but I'm a fan of proper
capitalization, so I tend avoid PewPewPew. I like <strong
class="pewpewpew">Pew-Pew-Pew</strong> myself, but I understand it may be
annoying to type; Pew Pew Pew, Pewpewpew or, for the nerds, Pew<sup>3</sup> are
alright too. More exotic wording and spelling, like PEWpewPEW, will make your
mom unlove you.</p> <p>The main things to remember are:</p>

<ol>
    <li>I don't really care</li>
    <li>You can change it and call it whatever you want.</li>
    <li>If you use the framework, you want to hide its name from the public.</li>
</ol>

<p>If, after all the derision I've managed to write in this documentation you
are still interested in <strong class="pewpewpew">Pew-Pew-Pew</strong>, well,
you're a psycopath and I welcome you pathology. Send me an email and tell me
about the ordeal of using it.</p>

