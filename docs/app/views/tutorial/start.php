<h2>Files and folders</h2>

<p>Getting the framework files should be easier, but for now they come in a
ZIP file. <a href="http://ifcanduela.kodingen.com/get-pew/" title="Pew-Pew-Pew framework">Download that nao</a>.</p>

<p>Unzip the archive in your <code>htdocs</code> folder, so you have something like this:</p>

<pre>
    ...
     |  htdocs
         |  pew-pew-pew
             | app
             | docs
             | sys
             | .htaccess
             | index.php
</pre>

<p>Of course, you may change the folder name to one that better fits 
your project. For this tutorial, it will be <em>blog</em>.</p>
