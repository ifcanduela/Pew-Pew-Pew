<h2>Ye Olde Blog Tutorial</h2>

<p>Just so we're clear, 
<a href="http://book.cakephp.org/view/1528/Blog">CakePHP</a>, 
<a href="http://lithify.me/docs/manual/quickstart">Lithium</a>, 
<a href="http://guides.rubyonrails.org/getting_started.html">Rails</a> and 
<a href="http://www.yiiframework.com/doc/blog/">Yii</a>
also do this 20-minute blog  stuff. It's industry-standard by now.</p>

<p>So yeah, a blog tutorial. This one will be a little longer than 20 minutes, 
though, since I'll try to explain a little more than, for example, the 
CakePHP one, and I like to write a lot. You can probably skim through the
pages and have your makeshift blog up and running in about half an 
hour, but I think the interesting part of this tutorial is not the end 
result.</p>

<p>In this tutorial, you'll be reading confusing instructions to build a
simple blog, consisting, more or less, of the following steps (in a different
order):</p>

<ol>
    <li>Putting the files in place</li>
    <li>Learn new meanings for some common words</li>
    <li>Configure the application</li>
    <li>Setup a SQLite database</li>
    <li>Create a Posts class so you can index, add and edit your posts</li>
    <li>Create view files to display the results of the controller's actions</li>
    <li>Give up</li>
</ol>

<p>I'm not serious about that last one, and there's more to the application
that what I put up there. Let's go, people.</p>
