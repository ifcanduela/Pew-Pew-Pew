<h2>Useful tools for the PHP developer</h2>

<p class="sidenote">You'll need a
<a title="Wikipedia - List of text editors" href="http://en.wikipedia.org/wiki/List_of_text_editors">text editor</a>,
a web server with PHP support (<a title="XAMPP" href="http://www.apachefriends.org/xampp">this</a>
for local development, and I recommend getting a free account
<a href="http://kodingen.com/" title="Kodingen">here</a>
if you want to have your project online) and a SQL front-end: <a href="http://www.phpmyadmin.net/">PHPMyAdmin</a> works
alright for MySQL (though I myself prefer <a href="http://heidisql.com/">HeidiSQL</a>)
and <a href="www.yunqa.de/delphi/doku.php/products/sqlitespy/index">SQLiteSpy</a>
gets the job done for SQLite.</p>

<h3>Beyond the basics</h3>

<p>You may want to give NetBeans a go. It's easy to use, fast, and fully-featured.
It also has great integration with <a href="<?php url('book/xdebug'); ?>">Xdebug</a></p>

<h3>Online resources</h3>

<dl>
    <dt>Learning</dt>
    
    <dd><a href="http://www.php.net/">PHP.net</a> is the main PHP resource.
    The reference manual is very comprehensive, it's well-written and the user
    comments add a lot of value to every single page.</dd>
    
    <dd>The <a href="http://developers.mozilla.org/">Mozilla Developer Network</a>
    is the ultimate reference for HTML, CSS and JavaScript. Avoid W3Schools
    like a plague (Google lets you banish some sites from your results).</dd>
    
    <dd><a href="http://net.tutsplus.com/">NetTutsPlus</a> usually posts development
    articles and videos. Their PHP content has focused on
    <a href="http://codeigniter.com/">CodeIgniter</a>, which is good thing.</dd>
    
    <dt>Tools and libraries</dt>
    
    <dd><a href="http://codepad.org">Codepad.org</a> is an online compiler
    and interpreter for a lot of languages, including PHP and JavaScript. I
    usually go there for fast calculations like MD5 hashses or timestamps.</dd>
    
    <dd>The <a href="http://getskeleton.com/">Skeleton grid</a> system is a CSS
    framework in the same vein of the <a href="http://960.gs/">960 Grid</a>, but
    I think it's easier to use (at the cost, I'm sure, of some advanced
    functionality).</dd>
</dl>
